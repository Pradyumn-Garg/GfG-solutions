class Solution{
    public:
    /* Should return data of middle node. If linked list is empty, then  -1*/
    int getMiddle(Node *head)
    {
        // Your code here
        if(head->next==NULL){
            return head->data;
        }
        Node *curr=head;
        int ans,flag=0;
        while(head->next!=NULL){
            if(flag==0){
                curr=curr->next;
                ans=curr->data;
                flag=1;
            }
            else if(flag==1){
                flag=0;
                ans=curr->data;
            }
            head=head->next;
        }
        return ans;
    }
};


